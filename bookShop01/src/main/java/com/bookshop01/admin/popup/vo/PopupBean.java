package com.bookshop01.admin.popup.vo;

public class PopupBean {
	private String popup_id;
	private String member_id;
	private String popup_type;
	private String popup_message;
	private String popup_imagename;
	private String credate;
	private String goods_id;
	
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}
	public String getPopup_id() {
		return popup_id;
	}
	public void setPopup_id(String popup_id) {
		this.popup_id = popup_id;
	}
	public String getMember_id() {
		return member_id;
	}
	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}
	public String getPopup_type() {
		return popup_type;
	}
	public void setPopup_type(String popup_type) {
		this.popup_type = popup_type;
	}
	public String getPopup_message() {
		return popup_message;
	}
	public void setPopup_message(String popup_message) {
		this.popup_message = popup_message;
	}
	public String getPopup_imagename() {
		return popup_imagename;
	}
	public void setPopup_imagename(String popup_imagename) {
		this.popup_imagename = popup_imagename;
	}
	public String getCredate() {
		return credate;
	}
	public void setCredate(String credate) {
		this.credate = credate;
	}
	
	

}
