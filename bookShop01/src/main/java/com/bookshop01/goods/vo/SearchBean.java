package com.bookshop01.goods.vo;

public class SearchBean {
	private String search_word_id;
	private String search_word;
	private String search_count;
	private String credate;
	
	public String getSearch_word_id() {
		return search_word_id;
	}
	public void setSearch_word_id(String search_word_id) {
		this.search_word_id = search_word_id;
	}
	public String getSearch_word() {
		return search_word;
	}
	public void setSearch_word(String search_word) {
		this.search_word = search_word;
	}
	public String getSearch_count() {
		return search_count;
	}
	public void setSearch_count(String search_count) {
		this.search_count = search_count;
	}
	public String getCredate() {
		return credate;
	}
	public void setCredate(String credate) {
		this.credate = credate;
	}
	
	
	

}
